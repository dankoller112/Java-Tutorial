
public class ForSchleife {

	public static void main(String[] args) {
		// Auch als "Zählerschleife" bezeichnet
		// Wird verwendet, wenn die Anzahl der Durchläufe bekannt ist
		// Besitzt Zählervariable, die aktuellen Durchlauf beinhaltet
		
		// Zählervariable wird üblicherweise als "i" bezeichnet
		
		// Bestandteile:
		// Initialiseriung Zählervariabe; Bedingung*; Zählervariable um 1 erhöht, wenn Codeblock ausgeführt
		// *) Wenn Bedingung "true", wird Codeblock ausgeführt
		for(int i = 0; i <= 10; i++) {
			System.out.println(i);
		}

	}

}
