
public class Variablen {

	public static void main(String[] args) {
		// #02 "Variablen"
		
		int age = 24; 	// Initialisierung
		
		int age2; 		// Deklaration
		age2 = 20; 		// Zuweisung
		
		System.out.println(age - age2);
		
		age2 = 21;
		
		System.out.println(age2);

	}

}
