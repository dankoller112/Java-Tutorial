
public class ArithmetischeOperatoren {

	public static void main(String[] args) {
		
		int num1 = 3;
		int num2 = 5;
		int result = num1 + num2;
		System.out.println(result); // Berechnung erfolgt in der Variable "result"
		System.out.println(num1 + num2); // Wird als Ausdruck aufgefasst, Berechnung in "println" Methode
		System.out.println(num1 + "" + num2); // Keine Berechnung, Ausgabe als "verknÃ¼pfter" Wert
	}

}
